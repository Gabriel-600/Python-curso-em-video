num = input ('Digite algo ')


print('É alfabético? {}'.format(num.isalpha()))                              # print ('É alfabetico? {}(num) ()' .isalpha  .format) 
print('É alfanumérico? {}'.format(num.isalnum()))                            # print ('num'.isalnum ())   
print('É decimal? {}'.format(num.isdecimal()))                               # print ('num'.isdecimal ())   
print('Está em maiúsculas? {}'.format(num.isupper()))                        # print ('num'.isupper ())        < TINHA ACERTADO   
print('Está em minúsculas? {}'.format(num.islower()))                        # print ('num'.islower  ())



                                                                                

