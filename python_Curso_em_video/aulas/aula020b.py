#       SOMAR

def soma(a, b):
    print(f'A = {a} B = {b}')
    s = a + b
    print(f'A soma entre A + B = {s}')
    print(s)


def lin():
    print('-'*40)

lin()
soma(10,20)
soma(2,5)
lin()

#       INVERTI
soma(b=4, a=6)



